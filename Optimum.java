
public class Optimum {
	String s;
	int cost;
	public Optimum(String s, int cost) {
		super();
		this.s = s;
		this.cost = cost;
	}
	public Optimum() {
		super();
	}
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}

}
